class UnixSocketClient final {
public:
    // [BUG-IPC-02 FIX]: Returns unique_ptr. Callers: cliRes.value->send(msg)

    [[nodiscard]] static Result<std::unique_ptr<UnixSocketClient>> connect(
        const std::string& socketPath,
        SecBytes           channelKey)
    {
        auto pathCheck = InputValidator::isValidSocketPath(socketPath);
        if (pathCheck.fail())
            return Result<std::unique_ptr<UnixSocketClient>>::Failure(
                pathCheck.status,
                "UnixSocketClient::connect: " + pathCheck.message);

        ::signal(SIGPIPE, SIG_IGN); // [N05]

        int fd = ::socket(AF_UNIX, SOCK_STREAM, 0);
        if (fd < 0)
            return Result<std::unique_ptr<UnixSocketClient>>::Failure(
                SecurityStatus::ERR_NETWORK_FAIL,
                std::string("socket: ") + ::strerror(errno));

        struct sockaddr_un addr{};
        addr.sun_family = AF_UNIX;
        std::strncpy(addr.sun_path, socketPath.c_str(), sizeof(addr.sun_path) - 1);

        if (::connect(fd, reinterpret_cast<sockaddr*>(&addr), sizeof(addr)) != 0) {
            int e = errno; ::close(fd);
            return Result<std::unique_ptr<UnixSocketClient>>::Failure(
                SecurityStatus::ERR_NETWORK_FAIL,
                std::string("connect: ") + ::strerror(e));
        }

        // [N06]: timeouts immediately after connect
        struct timeval tv{ IPC_RECV_TIMEOUT_SEC, 0 };
        ::setsockopt(fd, SOL_SOCKET, SO_RCVTIMEO, &tv, sizeof(tv));
        tv.tv_sec = IPC_SEND_TIMEOUT_SEC;
        ::setsockopt(fd, SOL_SOCKET, SO_SNDTIMEO, &tv, sizeof(tv));

        struct Priv : UnixSocketClient {
            Priv(int f, SecBytes k) : UnixSocketClient(f, std::move(k)) {}
        };
        return Result<std::unique_ptr<UnixSocketClient>>::Success(
            std::make_unique<Priv>(fd, std::move(channelKey)));
    }

    [[nodiscard]] Result<void>       send(const IpcMessage& msg) {
        return sendMessage(fd_, msg, channelKey_);
    }
    [[nodiscard]] Result<IpcMessage> recv() {
        return recvMessage(fd_, channelKey_);
    }

    ~UnixSocketClient() noexcept {
        if (fd_ >= 0) { ::close(fd_); fd_ = -1; }
    }

    UnixSocketClient(const UnixSocketClient&)            = delete;
    UnixSocketClient& operator=(const UnixSocketClient&) = delete;
    UnixSocketClient(UnixSocketClient&&)                 = delete;
    UnixSocketClient& operator=(UnixSocketClient&&)      = delete;

protected:
    UnixSocketClient(int fd, SecBytes key)
        : fd_(fd), channelKey_(std::move(key)) {}

private:
    int      fd_         { -1 };
    SecBytes channelKey_ {};
};

