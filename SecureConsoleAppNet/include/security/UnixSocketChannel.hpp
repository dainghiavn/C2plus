class UnixSocketServer final {
public:
    using ConnectionHandler = std::function<void(
        detail::SocketFd   fd,
        const IpcPeerInfo& peer,
        const SecBytes&    channelKey,
        SecureLogger&      logger)>;

    // [BUG-IPC-02 FIX]: Returns unique_ptr — UnixSocketServer is not
    // default-constructible (owns listenFd_), so Result<UnixSocketServer>
    // won't compile. Callers: srvRes.value->run(handler)

    [[nodiscard]] static Result<std::unique_ptr<UnixSocketServer>> create(
        const std::string& socketPath,
        SecBytes           channelKey,
        SecureLogger&      logger,
        uid_t              allowedUid = static_cast<uid_t>(-1))
    {
        // [BUG-E04]: isValidSocketPath() enforces UNIX_PATH_MAX (108 bytes)
        auto pathCheck = InputValidator::isValidSocketPath(socketPath);
        if (pathCheck.fail())
            return Result<std::unique_ptr<UnixSocketServer>>::Failure(
                pathCheck.status, "UnixSocketServer::create: " + pathCheck.message);

        ::signal(SIGPIPE, SIG_IGN);                      // [N05 FIX]
        detail::cleanupSocketFile(socketPath);            // [N01 FIX]: no TOCTOU

        int fd = ::socket(AF_UNIX, SOCK_STREAM, 0);
        if (fd < 0)
            return Result<std::unique_ptr<UnixSocketServer>>::Failure(
                SecurityStatus::ERR_NETWORK_FAIL,
                std::string("socket: ") + ::strerror(errno));

        struct sockaddr_un addr{};
        addr.sun_family = AF_UNIX;
        std::strncpy(addr.sun_path, socketPath.c_str(), sizeof(addr.sun_path) - 1);

        if (::bind(fd, reinterpret_cast<sockaddr*>(&addr), sizeof(addr)) != 0) {
            int e = errno; ::close(fd);
            return Result<std::unique_ptr<UnixSocketServer>>::Failure(
                SecurityStatus::ERR_NETWORK_FAIL, std::string("bind: ") + ::strerror(e));
        }

        // Restrict socket file to owner (mode 0600) — first layer of defence
        if (::chmod(socketPath.c_str(), 0600) != 0) {
            int e = errno; ::close(fd); detail::cleanupSocketFile(socketPath);
            return Result<std::unique_ptr<UnixSocketServer>>::Failure(
                SecurityStatus::ERR_NETWORK_FAIL,
                std::string("chmod(0600) socket: ") + ::strerror(e));
        }

        if (::listen(fd, IPC_LISTEN_BACKLOG) != 0) {
            int e = errno; ::close(fd); detail::cleanupSocketFile(socketPath);
            return Result<std::unique_ptr<UnixSocketServer>>::Failure(
                SecurityStatus::ERR_NETWORK_FAIL,
                std::string("listen: ") + ::strerror(e));
        }

        uid_t effectiveUid = (allowedUid == static_cast<uid_t>(-1))
            ? ::getuid() : allowedUid;

        // Local helper subclass so make_unique can reach protected ctor
        struct Priv : UnixSocketServer {
            Priv(int f, std::string p, SecBytes k, SecureLogger& l, uid_t u)
                : UnixSocketServer(f, std::move(p), std::move(k), l, u) {}
        };
        auto ptr = std::make_unique<Priv>(
            fd, socketPath, std::move(channelKey), logger, effectiveUid);

        return Result<std::unique_ptr<UnixSocketServer>>::Success(std::move(ptr));
    }

    // ── run — blocking accept loop ────────────────────────────────────────────
    // [N04 FIX]: SO_PEERCRED verified on every socket before data
    // [N06 FIX]: Timeouts applied immediately after accept()
    // [BUG-E06]: EINTR retry

    void run(ConnectionHandler handler) {
        running_.store(true, std::memory_order_relaxed);
        logger_.info("UnixSocketServer: listening on " + socketPath_);

        while (running_.load(std::memory_order_relaxed)) {
            int clientFd = ::accept(listenFd_, nullptr, nullptr);

            if (clientFd < 0) {
                int e = errno;
                if (e == EINTR) continue;                // [BUG-E06]: not an error
                if (!running_.load(std::memory_order_relaxed)) break;
                logger_.error("accept: " + std::string(::strerror(e)));
                continue;
            }

            // [N06]: timeouts before reading anything
            auto toRes = detail::applyTimeouts(clientFd);
            if (toRes.fail()) {
                logger_.warn("applyTimeouts: " + toRes.message);
                ::close(clientFd); continue;
            }

            // [N04]: peer credentials before data
            auto peerRes = detail::verifyPeer(clientFd);
            if (peerRes.fail()) {
                logger_.warn("SO_PEERCRED: " + peerRes.message);
                ::close(clientFd); continue;
            }

            const IpcPeerInfo& peer = peerRes.value;
            if (peer.uid != allowedUid_) {
                logger_.warn("rejected UID=" + std::to_string(peer.uid) +
                             " PID=" + std::to_string(peer.pid) +
                             " (allowed=" + std::to_string(allowedUid_) + ")");
                ::close(clientFd); continue;
            }

            logger_.info("accepted PID=" + std::to_string(peer.pid) +
                         " UID=" + std::to_string(peer.uid));

            try { handler(clientFd, peer, channelKey_, logger_); }
            catch (const std::exception& e) {
                logger_.error("handler exception: " + std::string(e.what()));
            } catch (...) {
                logger_.error("handler threw unknown exception");
            }

            ::close(clientFd);
        }

        logger_.info("UnixSocketServer: accept loop exited");
    }

    void stop() noexcept {
        running_.store(false, std::memory_order_relaxed);
        if (listenFd_ >= 0) ::shutdown(listenFd_, SHUT_RDWR);
    }

    // [N07 FIX]: RAII — socket file removed on destructor
    ~UnixSocketServer() noexcept {
        stop();
        if (listenFd_ >= 0) { ::close(listenFd_); listenFd_ = -1; }
        if (!socketPath_.empty()) detail::cleanupSocketFile(socketPath_);
    }

    UnixSocketServer(const UnixSocketServer&)            = delete;
    UnixSocketServer& operator=(const UnixSocketServer&) = delete;
    UnixSocketServer(UnixSocketServer&&)                 = delete;
    UnixSocketServer& operator=(UnixSocketServer&&)      = delete;

protected:
    UnixSocketServer(int fd, std::string path,
                     SecBytes key, SecureLogger& log, uid_t uid)
        : listenFd_(fd), socketPath_(std::move(path))
        , channelKey_(std::move(key)), logger_(log)
        , allowedUid_(uid), running_(false) {}

private:
    int               listenFd_   { -1 };
    std::string       socketPath_ {};
    SecBytes          channelKey_ {};
    SecureLogger&     logger_;
    uid_t             allowedUid_ { 0 };
    std::atomic<bool> running_    { false };
};

